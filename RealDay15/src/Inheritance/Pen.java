package Inheritance;

public class Pen {

	
	
	 protected int amount; //남은양
	 protected int width; //굵기
	 protected String color; // 색
	 
	 
	 
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	 
	 
}
